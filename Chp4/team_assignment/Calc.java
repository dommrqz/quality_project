public class Calc
{
   static public int add (int a, int b)
   {
      return a + b;
   }
   
   static public int substract (int a, int b)
   {
      return a - b;
   }
   
   static public int multiply (int a, int b)
   {
      return a * b;
   }
   
   static public float divide (int a, int b)
   {
      return (float)a / (float)b;
   }
}